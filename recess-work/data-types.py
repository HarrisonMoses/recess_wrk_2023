# 1. Numeric data types:
#    - int: represents integers, e.g., 1, -5, 100
#    - float: represents floating-point numbers, e.g., 3.14, -2.5, 0.75
#    - complex: represents complex numbers with real and imaginary parts, e.g., 2 + 3j, -1j

# 2. Text data type:
#    - str: represents a sequence of characters enclosed in single quotes ('') or double quotes (""), e.g., 'Hello', "Python"

# 3. Sequence data types:
#    - list: represents an ordered collection of items, enclosed in square brackets ([]), e.g., [1, 2, 3], ['apple', 'banana']
#    - tuple: represents an ordered, immutable collection of items, enclosed in parentheses (()), e.g., (1, 2, 3), ('a', 'b')
#    - range: represents a sequence of numbers, often used in loops and iterations, e.g., range(1, 10), range(0, 20, 2)

# 4. Mapping data type:
#    - dict: represents a collection of key-value pairs enclosed in curly braces ({}), e.g., {'name': 'John', 'age': 25}

# 5. Set data type:
#    - set: represents an unordered collection of unique items, enclosed in curly braces ({}), e.g., {1, 2, 3}

# 6. Boolean data type:
#    - bool: represents either True or False, used for logical operations and conditions, e.g., True, False

# 7. NoneType:
#    - None: represents a special value indicating the absence of a value or a null value



